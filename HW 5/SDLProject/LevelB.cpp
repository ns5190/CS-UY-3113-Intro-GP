LevelB::~LevelB()
{
    delete [] m_game_state.enemies;
    delete    m_game_state.player;
    delete    m_game_state.map;
    
    Mix_FreeMusic(m_game_state.bgm);
    Mix_FreeChunk(m_game_state.hit_sfx);
}

GLuint fontB_texture_id;

void LevelB::initialise()
{
    m_game_state.next_scene_id = -1;
    
    fontB_texture_id = Utility::load_texture(FONTB_FILEPATH);
    
    GLuint map_texture_id = Utility::load_texture("frame.png");
    m_game_state.map = new Map(LEVEL_WIDTH, LEVEL_HEIGHT, LEVELB_DATA, map_texture_id, 1.0f, 25, 1);
    

    int player_walking_animation[1][8] =
    {
        { 0, 1, 2, 3, 4, 5, 6, 7 },

    };
    

    glm::vec3 acceleration = glm::vec3(0.0f, -22.0f, 0.0f);
    
    GLuint player_texture_id = Utility::load_texture(SPRITESHEET_FILEPATH);
    
    m_game_state.player = new Entity(
        player_texture_id,         // texture id
        3.5f,                      // speed
        acceleration,              // acceleration
        15.0f,                      // jumping power
        player_walking_animation,  // animation index sets
        0.0f,                      // animation time
        4,                         // animation frame amount
        0,                         // current animation index
        8,                         // animation column amount
        1,                         // animation row amount
        1.3f,                      // width
        1.3f,                       // height
        PLAYER
    );
        
    m_game_state.player->set_position(glm::vec3(5.0f, -30.0f, 0.0f));
    m_game_state.player->set_scale(glm::vec3(2.0f, 2.0f, 0.0f));
    
     GLuint banana_bird_texture_id = Utility::load_texture(BANANA_BIRD_FILEPATH);
     
     m_game_state.enemies = new Entity[ENEMY_COUNT];
     
    for (int i = 0; i < ENEMY_COUNT; i++) {
        m_game_state.enemies[i] = Entity(
                                         banana_bird_texture_id,         // texture id
                                         2.0f,                      // speed
                                         glm::vec3(0.0f, 0.0f, 0.0f),              // acceleration
                                         15.0f,                     // jumping power
                                         player_walking_animation,  // animation index sets
                                         0.0f,                      // animation time
                                         4,                         // animation frame amount
                                         0,                         // current animation index
                                         8,                         // animation column amount
                                         1,                         // animation row amount
                                         1.3f,                      // width
                                         1.3f,                      // height
                                         ENEMY
                                         );
        
        m_game_state.enemies[i].set_movement(glm::vec3(-1.0f, 0.0f, 0.0f));
        m_game_state.enemies[i].set_ai_type(BANANA_BIRD);
        m_game_state.enemies[i].set_scale(glm::vec3(2.0f, 2.0f, 0.0f));
        m_game_state.enemies[i].move();
    }
    
    m_game_state.enemies[0].set_position(glm::vec3(20.0f, -20.0f, 0.0f));
    m_game_state.enemies[1].set_position(glm::vec3(50.0f, -20.0f, 0.0f));

    Mix_OpenAudio(44100, MIX_DEFAULT_FORMAT, 2, 4096);

    m_game_state.bgm = Mix_LoadMUS("level.mp3");
    Mix_PlayMusic(m_game_state.bgm, -1);

   m_game_state.hit_sfx = Mix_LoadWAV("hit.mp3");
}

void LevelB::update(float delta_time)
{

    if (m_game_state.player->get_play_crash_sound()) {
        Mix_PlayChannel(-1, m_game_state.hit_sfx, 0);
        m_game_state.player->set_play_crash_sound(false);
    }
    if (m_game_state.player->get_lose()) return;
    m_game_state.player->update(delta_time, m_game_state.player, m_game_state.enemies, ENEMY_COUNT, m_game_state.map);
    for (int i = 0; i < ENEMY_COUNT; i++) m_game_state.enemies[i].update(delta_time, m_game_state.player, m_game_state.player, 1, NULL);
    if (m_game_state.player->get_next_level()) m_game_state.next_scene_id = 3;
}

void LevelB::render(ShaderProgram *program)
{
    m_game_state.map->render(program);
    m_game_state.player->render(program);
    for (int i = 0; i < ENEMY_COUNT; i++) m_game_state.enemies[i].render(program);
}

